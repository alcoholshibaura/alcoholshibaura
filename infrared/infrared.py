def infraredInit(manager):
    profile = OpenRTM_aist.Properties(defaults_str=infrared_spec)
    manager.registerFactory(profile,
                            infrared,
                            OpenRTM_aist.Delete)

def MyModuleInit(manager):
    infraredInit(manager)

    # Create a component
    comp = manager.createComponent("infrared")

def main():
	mgr = OpenRTM_aist.Manager.init(sys.argv)
	mgr.setModuleInitProc(MyModuleInit)
	mgr.activateManager()
